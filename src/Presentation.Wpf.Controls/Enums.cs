namespace MaSch.Presentation.Wpf
{
    /// <summary>
    /// Specifies to wich side something is anchored.
    /// </summary>
    public enum AnchorStyle
    {
        /// <summary>
        /// Not anchored.
        /// </summary>
        None = 0,

        /// <summary>
        /// Anchored to the left.
        /// </summary>
        Left = 1,

        /// <summary>
        /// Anchored to the top.
        /// </summary>
        Top = 2,

        /// <summary>
        /// Anchored to the right.
        /// </summary>
        Right = 3,

        /// <summary>
        /// Anchored to the bottom.
        /// </summary>
        Bottom = 4,
    }
}
